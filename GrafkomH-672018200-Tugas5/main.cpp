void onKeyPressed(unsigned char key, int x, int y) {
	cout << key << endl;
	switch (key) {
	case 'w':
	case 'W':
		glTranslatef(0.0, 0.0, step);
		break;
	case 'd':
	case 'D':
		glTranslatef(step, 0.0, 0.0);
		break;
	case 's':
	case 'S':
		glTranslatef(0.0, 0.0, -step);
		break;
	case 'a':
	case 'A':
		glTranslatef(-step, 0.0, 0.0);
		break;
	case 'i':
	case 'I':
		glTranslatef(0.0, 0.0, step);
		break;
	case 'k':
	case 'K':
		glTranslatef(0.0, 0.0, -step);
		break;

	case ' ':
		if (isDepth) {
			glDisable(GL_DEPTH_TEST);
		}
		else {
			glEnable(GL_DEPTH_TEST);
		}
		isDepth = !isDepth;
		break;
	}
	tampil();
}

void onSpecialKeyPressed(int key, int x, int y) {
	switch (key) {
	case GLUT_KEY_UP:
		glRotatef(doubleAngle, rotationStep ,0.0, 0.0);
		break;
	case GLUT_KEY_DOWN:
		glRotatef(doubleAngle, -rotationStep, 0.0, 0.0);
		break;
	case GLUT_KEY_LEFT:
		glRotatef(doubleAngle, 0.0, rotationStep, 0.0);
		break;
	case GLUT_KEY_RIGHT:
		glRotatef(doubleAngle, 0.0, -rotationStep, 0.0);
		break;
	}
	tampil();
}

